package util;

public class Mth {
	public static float sin(float n) {
		return (float) Math.sin(n);
	}

	public static float cos(float n) {
		return (float) Math.cos(n);
	}

	public static float sqrt(float n) {
		return (float) Math.sqrt(n);
	}
}
